age = 18
print(f"今年已经：{age}岁了")
"""
if 要判断的条件 冒号
     条件成立要做的事 
"""
if age >= 18:
    # 如果if判断结果为True，执行以下内容,如果结果为False则不会执行
    print("我已经成年了")
    print("时间等不了人")

# 没有空格缩进不属于if
print("我不受影响")

if age <= 10:
    # 如果if判断结果为True，执行以下内容,如果结果为False则不会执行
    print("我已经成年了")
    print("时间等不了人")


# 判断语句的结果，必须是布尔类型True or False
